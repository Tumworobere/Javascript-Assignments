//Create a function that checks if the input/parameter is an even number.
function checkEvenNumber(number) {
    if (number % 2 === 0) {
        console.log(`${number} is an even number.`);
    } else {
        console.log(`${number} is not an even number.`);
    }
}

checkEvenNumber(11);


